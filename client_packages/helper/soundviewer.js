var index = 0;
var PlaySound = false;
var text = "";

mp.events.add("Client:ShowSoundViewer", () => {
    PlaySound = !PlaySound;
    if (PlaySound == true) {
        mp.events.call('Client:SendNotificationWithoutButton', 'Soundviewer aktiviert!', 'success', 'top-left', 2500);
    } else {
        mp.events.call('Client:SendNotificationWithoutButton', 'Soundviewer deaktiviert!', 'success', 'top-left', 2500);
    }
})

mp.keys.bind(0x25, true, function () {
    if (!PlaySound) return;
    if (index - 1 >= 0) index--;
    else index = sounds.length - 1;
    playSound();
});

mp.keys.bind(0x27, true, function () {
    if (!PlaySound) return;
    if (index + 1 <= sounds.length) index++;
    else index = 0;
    playSound();
});

function playSound() {
    sounds.forEach((sound) => {
        if (sound.id === index) {
            mp.game.audio.playSoundFrontend(-1, sound.soundName, sound.soundSetName, true);
            text = 'Id: ~b~' + index + ' ~w~SoundName: ~b~' + sound.soundName + ' ~w~SoundSetName: ~b~' + sound.soundSetName;
        }
    });
}

mp.events.add('render', () => {
    if (PlaySound && !mp.players.local.vehicle) {
        let adminduty = mp.players.local.getVariable('Player:AdminLogin');
        if (!adminduty) {
            PlaySound = false;
        }
        mp.game.graphics.drawText(text, [0.5, 0.9], {
            font: 0,
            color: [255, 255, 255, 185],
            scale: [0.4, 0.4],
            outline: true
        });
    }
});

mp.events.add("playerDeath", (player, reason, killer) => {
    PlaySound = false;
});